#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xA2,0xA8,0xAA,0x51,0xB4,0xFD,0x0C,0x59,0x94,0x92,0x36,0x43,
		0x47,0x72,0x0E,0xD4,0x47,0x1D,0x7D,0x2C,0xDA,0xF4,0x07,0x18,
		0x58,0x41,0x85,0xCC,0x0F,0x0F,0xC5,0x94,0x63,0xA5,0x48,0xCA,
		0x13,0xF8,0x84,0x6D,0x3E,0xF1,0x16,0x07,0x94,0x48,0xEF,0xB4,
		0x00,0x8C,0xF7,0xD1,0x70,0x6B,0xA5,0xB8,0x89,0x35,0xEA,0x91,
		0x52,0xB5,0xAA,0x03,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
